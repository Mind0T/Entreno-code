#include <stdio.h>

int main()
    {
        //operador de asigncion
        int  miNumero=10;
        printf("Valor de miNumero:%d\n",miNumero);
        miNumero=20;
        printf("Valor de miNumero:%d\n",miNumero);
        //operador +=
        miNumero+=5;
        printf("Valor de miNumero:%d\n",miNumero);
        //operador -=
        miNumero-=5;
        printf("Valor de miNumero:%d\n",miNumero);
        miNumero*=2;
        printf("Valor de miNumero:%d\n",miNumero);
        miNumero/=2;
        printf("Valor de miNumero:%d\n",miNumero);
        miNumero%=3;
        printf("Valor de miNumero:%d\n",miNumero);
        return 0;

    }